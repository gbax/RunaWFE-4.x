package ru.runa.wfe.graph.history.model;

import java.awt.Point;

public class BendpointModel {
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    // TODO Used for compatibility
    // Remove if in all gpd.xml bendpoints have absolute {x, y} coords
    public boolean isCompatibilityModel() {
        return x == 0 && y == 0;
    }

    private int h1;
    private int h2;
    private int w1;
    private int w2;

    public int getH1() {
        return h1;
    }

    public void setH1(int h1) {
        this.h1 = h1;
    }

    public int getH2() {
        return h2;
    }

    public void setH2(int h2) {
        this.h2 = h2;
    }

    public int getW1() {
        return w1;
    }

    public void setW1(int w1) {
        this.w1 = w1;
    }

    public int getW2() {
        return w2;
    }

    public void setW2(int w2) {
        this.w2 = w2;
    }

    public Point getPointFrom(Point point) {
        return new Point(point.x + w1, point.y + h1);
    }

    public Point getPointTo(Point point) {
        return new Point(point.x + w2, point.y + h2);
    }
}
