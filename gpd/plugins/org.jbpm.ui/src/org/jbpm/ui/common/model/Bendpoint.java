package ru.runa.bpm.ui.common.model;

public class Bendpoint {
    private int x;

    private int y;

    public Bendpoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
